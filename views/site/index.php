<h1>Documentation</h1>
<hr>
<code>[http://api.504.bumbu.tv/web/lessons]</code> - will return all available lessons divided by pagination <br>
<code>[http://api.504.bumbu.tv/web/lessons/1]</code> - will return only the first lesson's data (!WITHOUT WORDS) <br>
<code>[http://api.504.bumbu.tv/web/lessons/1?expand=words]</code> - will return all words which contain in lesson 1 along with it's data <br>
<code>[http://api.504.bumbu.tv/web/words]</code> - will return all words available divided by pagination <br>
<code>[http://api.504.bumbu.tv/web/words/1]</code> - will return only the first word's data <br>
